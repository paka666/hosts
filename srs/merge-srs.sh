merge_group()
{
  local GROUP_NAME=$1
  shift
  local URLS=("$@")
  local LOCAL_SRS_FILE="${URLS[0]}"
  local TIMESTAMP
  TIMESTAMP=$(date -u +%Y%m%dT%H%M%SZ)

  rm -f temp/input-"$GROUP_NAME"-*.srs

  if [ -f "$LOCAL_SRS_FILE" ] && [ -s "$LOCAL_SRS_FILE" ]; then
    cp "$LOCAL_SRS_FILE" "temp/input-$GROUP_NAME-0.srs"
  else
    echo "Warning: local file $LOCAL_SRS_FILE not found or is empty. Starting fresh."
    mkdir -p "$(dirname "$LOCAL_SRS_FILE")"
  fi

  local i=1
  local pids=()
  for url in "${URLS[@]:1}"; do
    if [ -z "$url" ]; then
      continue
    fi

    local current_i=$i
    (
      local file_index=$current_i
      local output_file="temp/input-$GROUP_NAME-$file_index.srs"
      echo "Downloading $url to $output_file"
      wget -q --timeout=180 --tries=3 "$url" -O "$output_file" \
        || { echo "Warning: failed to download $url (group $GROUP_NAME)"; rm -f "$output_file"; }
    ) &
    pids+=($!)

    ((i++))
  done

  if [ ${#pids[@]} -gt 0 ]; then
    echo "Waiting for ${#pids[@]} downloads for group $GROUP_NAME..."
    wait "${pids[@]}" 2>/dev/null
    echo "Downloads for $GROUP_NAME finished."
  fi

  shopt -s nullglob
  local inputs=(temp/input-"$GROUP_NAME"-*.srs)
  shopt -u nullglob

  if [ "${#inputs[@]}" -eq 0 ]; then
    echo "Error: no input files available for group $GROUP_NAME  skipping merge."
    return 1
  fi

  echo "Found ${#inputs[@]} input files for group $GROUP_NAME"

  local config_file="temp/config-$GROUP_NAME.json"
  cat > "$config_file" << EOF
{
  "version": 1.12.12,
  "rule_set": [
EOF

  local first=true
  for input_file in "${inputs[@]}"; do
    if [ "$first" = true ]; then
      first=false
    else
      echo "," >> "$config_file"
    fi
    local rule_tag="rule_$(basename "$input_file" .srs)"
    local abs_path
    abs_path=$(realpath "$input_file")
    cat >> "$config_file" << EOF
    {
      "tag": "$rule_tag",
      "type": "local",
      "format": "binary",
      "path": "$abs_path"
    }
EOF
  done

  cat >> "$config_file" << EOF
  ]
}
EOF

  echo "Generated config file for $GROUP_NAME with ${#inputs[@]} rule sets"

  echo "Config file preview (first 100 lines):"
  head -100 "$config_file"

  local backup="srs/${GROUP_NAME}.srs.bak.${TIMESTAMP}"
  if [ -f "$LOCAL_SRS_FILE" ]; then
    cp -a "$LOCAL_SRS_FILE" "$backup"
  fi

  echo "Compiling merged rule set for $GROUP_NAME..."
  if sing-box rule-set compile "$config_file" -o "$LOCAL_SRS_FILE"; then
    echo "Successfully compiled $LOCAL_SRS_FILE"
    cp -a "$LOCAL_SRS_FILE" "srs/${GROUP_NAME}.srs"
  else
    echo "Error: Failed to compile rule set for $GROUP_NAME"
    if [ -f "$backup" ]; then
      cp -a "$backup" "$LOCAL_SRS_FILE"
      echo "Restored from backup: $backup"
    fi
    return 1
  fi

  rm -f temp/input-"$GROUP_NAME"-*.srs "$config_file"
  echo "Merged group $GROUP_NAME -> $LOCAL_SRS_FILE (backup: $backup)"
}

